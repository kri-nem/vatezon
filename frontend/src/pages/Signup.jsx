import Avatar from '@mui/material/Avatar'
import Button from '@mui/material/Button'
import CssBaseline from '@mui/material/CssBaseline'
import TextField from '@mui/material/TextField'
import Link from '@mui/material/Link'
import Grid from '@mui/material/Grid'
import Box from '@mui/material/Box'
import LockOutlinedIcon from '@mui/icons-material/LockOutlined'
import Typography from '@mui/material/Typography'
import Container from '@mui/material/Container'
import { createTheme, ThemeProvider } from '@mui/material/styles'
import { useState } from 'react'
import { useNavigate } from 'react-router-dom'
import { ToastContainer, toast } from 'react-toastify'
import 'react-toastify/dist/ReactToastify.css'
import { postSignUp } from '../fetches.js'

const defaultTheme = createTheme()

const checkAllRequiredFields = (fields) => {
  for (let field of fields) {
    if (field === '') {
      return false
    }
  }
  return true
}

const notify = (boolean, message) => {
  if (boolean) {
    toast(message)
  } else {
    toast.error(message, {
      position: 'top-center',
      autoClose: 5000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
      theme: 'dark',
    })
  }
}

export default function Signup () {
  const navigate = useNavigate()
  const [username, setUsername] = useState('')
  const [firstname, setFirstname] = useState('')
  const [lastname, setLastname] = useState('')
  const [password, setPassword] = useState('')
  const [email, setEmail] = useState('')

  const handleSignup = (e) => {
    e.preventDefault()
    if (checkAllRequiredFields([username, firstname, lastname, password, email])) {
      const newUser = {
        'username': username, 'firstname': firstname, 'lastname': lastname, 'email': email, 'password': password,
      }
      postSignUp(newUser)
        .then(res => {
          if (res.ok) {
            navigate('/login')
          }
        })
    } else {
      notify(false, 'Please fill all required fields!')
    }
  }

  const style = {
    "& label.Mui-focused": {
      color: "darkgreen"
    },
    "& .MuiOutlinedInput-root": {
      "&.Mui-focused fieldset": {
        borderColor: "black"
      }
    }
  } 

  return (<>
      <ThemeProvider theme={defaultTheme}>
        <Container component="main" maxWidth="xs">
          <CssBaseline/>
          <Box
            sx={{
              marginTop: 8,
              display: 'flex',
              flexDirection: 'column',
              alignItems: 'center',
              backgroundColor: 'rgba(255, 241, 230, 0.5)',
              padding: '10%',
            }}
          >
            <Avatar sx={{ m: 1, bgcolor: '#85b883' }}>
              <LockOutlinedIcon/>
            </Avatar>
            <Typography component="h1" variant="h5" sx={{textDecoration: 'underline'}}>
              Sign up
            </Typography>
            <Box component="form" onSubmit={handleSignup} noValidate sx={{ mt: 3 }}>
              <Grid container spacing={2}>
                <Grid item xs={12} sm={6}>
                  <TextField
                    autoComplete="given-name"
                    name="firstName"
                    required
                    fullWidth
                    id="firstName"
                    label="First Name"
                    autoFocus
                    onChange={e => setFirstname(e.target.value)}
                    sx={style}
                  />
                </Grid>
                <Grid item xs={12} sm={6}>
                  <TextField
                    required
                    fullWidth
                    id="lastName"
                    label="Last Name"
                    name="lastName"
                    autoComplete="family-name"
                    onChange={e => setLastname(e.target.value)}
                    sx={style}
                  />
                </Grid>
                <Grid item xs={12}>
                  <TextField
                    required
                    fullWidth
                    id="username"
                    label="Username"
                    name="username"
                    autoComplete="username"
                    onChange={e => setUsername(e.target.value)}
                    sx={style}
                  />
                </Grid>
                <Grid item xs={12}>
                  <TextField
                    required
                    fullWidth
                    id="email"
                    label="Email Address"
                    name="email"
                    autoComplete="email"
                    onChange={e => setEmail(e.target.value)}
                    sx={style}
                  />
                </Grid>
                <Grid item xs={12}>
                  <TextField
                    required
                    fullWidth
                    name="password"
                    label="Password"
                    type="password"
                    id="password"
                    autoComplete="new-password"
                    onChange={e => setPassword(e.target.value)}
                    sx={style}
                  />
                </Grid>
              </Grid>
              <Button
                type="submit"
                fullWidth
                variant="contained"
                  sx={{
                    mt: 3,
                    mb: 2,
                    backgroundColor: '#407e65', // Normal button color
                    '&:hover': {
                      backgroundColor: '#85b883', // Color to change to on hover
                    }
                  }}
              >
                Sign Up
              </Button>
              <Grid container justifyContent="flex-end">
                <Grid item>
                  <Link href="#" variant="body2" sx={{ color: 'black', '&:hover': { color: 'green' }, textDecorationColor: 'black'}}>
                    Already have an account? Sign in
                  </Link>
                </Grid>
              </Grid>
            </Box>
          </Box>
        </Container>
      </ThemeProvider>
      <ToastContainer position="top-center"
                      autoClose={5000}
                      hideProgressBar={false}
                      newestOnTop={false}
                      closeOnClick
                      rtl={false}
                      pauseOnFocusLoss
                      draggable
                      pauseOnHover
                      theme="dark"/>
    </>)
}